package com.mazars.management.web.forms;
import com.google.gson.Gson;
import com.mazars.management.db.vo.*;

/**
 *
 * @author glazov
 */
public class EmployeesForProjectsReportForm {
    private Long subdepartmentId;
    private Long departmentId;
    private Long officeId;
    private Integer year;
    private Integer month;

    public EmployeesForProjectsReportForm() {
    }

    public Long getDepartmentId() {
        return departmentId;
    }

    public void setDepartmentId(Long departmentId) {
        this.departmentId = departmentId;
    }

    public Long getOfficeId() {
        return officeId;
    }

    public void setOfficeId(Long officeId) {
        this.officeId = officeId;
    }

    public Long getSubdepartmentId() {
        return subdepartmentId;
    }

    public void setSubdepartmentId(Long subdepartmentId) {
        this.subdepartmentId = subdepartmentId;
    }

    public Integer getMonth() {
        return month;
    }

    public void setMonth(Integer month) {
        this.month = month;
    }

    public Integer getYear() {
        return year;
    }

    public void setYear(Integer year) {
        this.year = year;
    }

    public static EmployeesForProjectsReportForm getFromJson(String json) {
        Gson gson = new Gson();
        return gson.fromJson(json, EmployeesForProjectsReportForm.class);
    }
}
